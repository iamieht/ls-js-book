let age = 20;
let yearsIncrement = 10;

console.log(`You are ${age} years old.`);
console.log(
  `In ${yearsIncrement} years, you will be ${age + yearsIncrement} years old.`
);
console.log(
  `In ${yearsIncrement * 2} years, you will be ${
    age + yearsIncrement * 2
  } years old.`
);
console.log(
  `In ${yearsIncrement * 3} years, you will be ${
    age + yearsIncrement * 3
  } years old.`
);
console.log(
  `In ${yearsIncrement * 4} years, you will be ${
    age + yearsIncrement * 4
  } years old.`
);
